#include "Move.h"

//------------------------------------------------------------------------------------------------
Move::Move() {
	Reset();
}

//------------------------------------------------------------------------------------------------
Move::~Move() {
}

//------------------------------------------------------------------------------------------------
void Move::Reset() {
	Position e(-1, -1);
	SetEnd(e);
}

//------------------------------------------------------------------------------------------------
void Move::SetStart(Position p) {
	start = p;
}

//------------------------------------------------------------------------------------------------
Position Move::GetStart() {
	return start;
}

//------------------------------------------------------------------------------------------------
void Move::SetEnd(Position p) {
	end = p;
}

//------------------------------------------------------------------------------------------------
Position Move::GetEnd() {
	return end;
}

//------------------------------------------------------------------------------------------------
void Move::SetColor(PC c) {
	color = c;
}

//------------------------------------------------------------------------------------------------
PC Move::GetColor() {
	return color;
}

//------------------------------------------------------------------------------------------------
void Move::SetType(Type t) {
	type = t;
}

//------------------------------------------------------------------------------------------------
Type Move::GetType() {
	return type;
}

//------------------------------------------------------------------------------------------------
void Move::SetCaptured(Type t) {
	capturedPiece = t;
}

//------------------------------------------------------------------------------------------------
Type Move::GetCaptured() {
	return capturedPiece;
}
