#include "Position.h"

//------------------------------------------------------------------------------------------------
Position::Position() : row(-1), col(-1) {
}

//------------------------------------------------------------------------------------------------
Position::Position(int r, int c) : row(r), col(c) {
}

//------------------------------------------------------------------------------------------------
Position::~Position() {
}

//------------------------------------------------------------------------------------------------
Position::Position(const Position & other) : row(other.row), col(other.col) {
}

//------------------------------------------------------------------------------------------------
int Position::GetRow() {
	return row;
}

//------------------------------------------------------------------------------------------------
int Position::GetCol() {
	return col;
}

//------------------------------------------------------------------------------------------------
void Position::SetRow (int r) {
	row = r;
}

//------------------------------------------------------------------------------------------------
void Position::SetCol (int c) {
	col = c;
}
//------------------------------------------------------------------------------------------------
